//! Deterministic workspace projections over the run substrate.
//!
//! Two renderers:
//! - `render_substrate_md` — live / retained / preserved runs.
//! - `render_compost_md` — distilled (gist kept) / decayed (tombstone only) runs.
//!
//! Both take `&[Run]` and return `String`. Determinism is load-bearing: rows are
//! stable-sorted by `(created_at, id)` within each section, and no generation
//! timestamp appears in the body. See ADR 0011.
//!
//! `run_maintenance` applies decay, renders both files into a workspace directory,
//! and appends a `maintenance` audit event.

use std::{fs, path::Path, path::PathBuf};

use serde::Serialize;

use crate::{AuditLog, Confidence, Db, DecayEngine, DecayReport, Run, Substrate};

// ── helpers ───────────────────────────────────────────────────────────────────

const HEADER: &str =
    "<!-- generated by Mycel; projection only; not an input surface. do not edit. -->\n";

fn escape_cell(value: &str) -> String {
    value.replace('|', "\\|").replace('\n', " ")
}

fn confidence_str(c: Confidence) -> &'static str {
    match c {
        Confidence::Solid => "solid",
        Confidence::Directional => "directional",
        Confidence::Vibes => "vibes",
    }
}

/// Sort runs deterministically: primary key `created_at`, secondary key `id` (lexicographic).
fn sort_runs(runs: &mut Vec<&Run>) {
    runs.sort_by(|a, b| a.created_at.cmp(&b.created_at).then(a.id.cmp(&b.id)));
}

// ── SUBSTRATE.md ──────────────────────────────────────────────────────────────

/// Render `SUBSTRATE.md` — active substrate snapshot.
///
/// Sections:
/// - `## live` — runs with `decay_state IS NULL` and `no_compost = false`.
/// - `## retained` — runs with `decay_state == "retained"`.
/// - `## preserved` — runs with `no_compost = true` (regardless of decay_state).
///
/// Each section is a markdown table `| id | kind | confidence | summary |`.
/// Empty section → `none`. Rows are stable-sorted by `(created_at, id)`.
pub fn render_substrate_md(runs: &[Run]) -> String {
    let mut output = format!("{HEADER}# substrate\n\n");

    // live: no decay_state yet and not no_compost
    let mut live: Vec<&Run> = runs
        .iter()
        .filter(|r| r.decay_state.is_none() && !r.no_compost)
        .collect();
    sort_runs(&mut live);

    // retained: decay_state == "retained"
    let mut retained: Vec<&Run> = runs
        .iter()
        .filter(|r| r.decay_state.as_deref() == Some("retained"))
        .collect();
    sort_runs(&mut retained);

    // preserved: no_compost == true
    let mut preserved: Vec<&Run> = runs.iter().filter(|r| r.no_compost).collect();
    sort_runs(&mut preserved);

    output.push_str("## live\n\n");
    write_substrate_section(&mut output, &live);

    output.push_str("\n## retained\n\n");
    write_substrate_section(&mut output, &retained);

    output.push_str("\n## preserved\n\n");
    write_substrate_section(&mut output, &preserved);

    output
}

fn write_substrate_section(output: &mut String, runs: &[&Run]) {
    if runs.is_empty() {
        output.push_str("none\n");
        return;
    }
    output.push_str("| id | kind | confidence | summary |\n");
    output.push_str("| --- | --- | --- | --- |\n");
    for run in runs {
        output.push_str(&format!(
            "| {} | {} | {} | {} |\n",
            run.id,
            run.kind.as_str(),
            confidence_str(run.confidence),
            escape_cell(&run.summary),
        ));
    }
}

// ── COMPOST.md ────────────────────────────────────────────────────────────────

/// Render `COMPOST.md` — post-decay tombstone record.
///
/// Sections:
/// - `## distilled` — runs with `decay_state == "distilled"`; shows `distilled_summary`.
/// - `## decayed` — runs with `decay_state == "decayed"`; tombstone only (id + confidence).
///
/// Empty section → `none`. Rows are stable-sorted by `(created_at, id)`.
pub fn render_compost_md(runs: &[Run]) -> String {
    let mut output = format!("{HEADER}# compost\n\n");

    let mut distilled: Vec<&Run> = runs
        .iter()
        .filter(|r| r.decay_state.as_deref() == Some("distilled"))
        .collect();
    sort_runs(&mut distilled);

    let mut decayed: Vec<&Run> = runs
        .iter()
        .filter(|r| r.decay_state.as_deref() == Some("decayed"))
        .collect();
    sort_runs(&mut decayed);

    output.push_str("## distilled\n\n");
    if distilled.is_empty() {
        output.push_str("none\n");
    } else {
        output.push_str("| id | confidence | distilled |\n");
        output.push_str("| --- | --- | --- |\n");
        for run in &distilled {
            let gist = run
                .distilled_summary
                .as_deref()
                .unwrap_or("")
                .replace('|', "\\|")
                .replace('\n', " ");
            output.push_str(&format!(
                "| {} | {} | {} |\n",
                run.id,
                confidence_str(run.confidence),
                gist,
            ));
        }
    }

    output.push_str("\n## decayed\n\n");
    if decayed.is_empty() {
        output.push_str("none\n");
    } else {
        output.push_str("| id | confidence |\n");
        output.push_str("| --- | --- |\n");
        for run in &decayed {
            output.push_str(&format!(
                "| {} | {} |\n",
                run.id,
                confidence_str(run.confidence),
            ));
        }
    }

    output
}

// ── maintenance cycle ─────────────────────────────────────────────────────────

/// Outcome of a single `run_maintenance` call.
pub struct MaintenanceReport {
    pub decay: DecayReport,
    pub substrate_path: PathBuf,
    pub compost_path: PathBuf,
}

/// Run one scheduled maintenance cycle as of unix timestamp `now`.
///
/// Order of operations:
/// 1. Apply decay via `DecayEngine`.
/// 2. List all runs (now with updated decay_state).
/// 3. Render `SUBSTRATE.md` and `COMPOST.md`.
/// 4. Write both files into `workspace_dir`.
/// 5. Append a `maintenance` audit event with decay counts.
///
/// Deterministic and idempotent (decay engine is idempotent; renderers are pure).
pub fn run_maintenance(
    db: &Db,
    workspace_dir: &Path,
    now: i64,
) -> crate::Result<MaintenanceReport> {
    // 1. Apply decay.
    let decay = DecayEngine::new(db).run(now)?;

    // 2. List runs after decay so decay_state is current.
    let runs = Substrate::new(db).list()?;

    // 3+4. Render and write.
    fs::create_dir_all(workspace_dir)?;

    let substrate_path = workspace_dir.join("SUBSTRATE.md");
    let compost_path = workspace_dir.join("COMPOST.md");

    fs::write(&substrate_path, render_substrate_md(&runs))?;
    fs::write(&compost_path, render_compost_md(&runs))?;

    // 5. Audit.
    let payload = MaintenanceAuditPayload {
        now,
        retained: decay.retained.len(),
        distilled: decay.distilled.len(),
        decayed: decay.decayed.len(),
        preserved: decay.preserved.len(),
        skipped_live: decay.skipped_live.len(),
    };
    AuditLog::new(db).append("maintenance", &payload)?;

    Ok(MaintenanceReport {
        decay,
        substrate_path,
        compost_path,
    })
}

#[derive(Serialize)]
struct MaintenanceAuditPayload {
    now: i64,
    retained: usize,
    distilled: usize,
    decayed: usize,
    preserved: usize,
    skipped_live: usize,
}

