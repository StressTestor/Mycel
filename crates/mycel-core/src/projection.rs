    // ── run_maintenance integration ───────────────────────────────────────────

    #[test]
    fn run_maintenance_writes_files_and_appends_audit_event() {
        let db = test_db();
        let sub = Substrate::new(&db);

        // Insert a speculative (Vibes) record with TTL 500 → expires at 500.
        sub.insert_with_decay(
            RunKind::Observation,
            RunStatus::Applied,
            "speculative hypothesis for maintenance test",
            Confidence::Vibes,
            Some(500),
            false,
        )
        .expect("insert vibes");

        // Insert a directional (Directional) record with TTL 500.
        sub.insert_with_decay(
            RunKind::Observation,
            RunStatus::Applied,
            "probable observation for maintenance test",
            Confidence::Directional,
            Some(500),
            false,
        )
        .expect("insert directional");

        // Use a temp dir for workspace.
        let workspace = tempfile::tempdir().expect("tempdir");

        // Run maintenance at now=1000 → both runs are expired.
        let report = run_maintenance(&db, workspace.path(), 1000).expect("run_maintenance");

        // Both files must exist.
        assert!(
            report.substrate_path.exists(),
            "SUBSTRATE.md must exist after maintenance"
        );
        assert!(
            report.compost_path.exists(),
            "COMPOST.md must exist after maintenance"
        );

        // SUBSTRATE.md must have the header.
        let substrate_md = fs::read_to_string(&report.substrate_path).expect("read SUBSTRATE.md");
        assert!(
            substrate_md.starts_with("<!-- generated by Mycel"),
            "SUBSTRATE.md must have generated header"
        );
        assert!(
            substrate_md.contains("# substrate"),
            "SUBSTRATE.md must contain # substrate heading"
        );

        // COMPOST.md must have distilled and decayed sections populated.
        let compost_md = fs::read_to_string(&report.compost_path).expect("read COMPOST.md");
        assert!(
            compost_md.contains("## distilled"),
            "COMPOST.md must have ## distilled section"
        );
        assert!(
            compost_md.contains("## decayed"),
            "COMPOST.md must have ## decayed section"
        );

        // Audit log must contain a maintenance event.
        let log = AuditLog::new(&db).list().expect("audit list");
        let maintenance_events: Vec<_> = log.iter().filter(|e| e.event == "maintenance").collect();
        assert!(
            !maintenance_events.is_empty(),
            "audit log must contain a maintenance event"
        );

        let payload = &maintenance_events[0].payload;
        assert_eq!(
            payload.get("now").and_then(|v| v.as_i64()),
            Some(1000),
            "maintenance audit payload must record the now timestamp"
        );
        // distilled from 1 directional + decayed from 1 vibes
        assert_eq!(
            payload.get("distilled").and_then(|v| v.as_u64()),
            Some(1),
            "maintenance audit payload must record distilled count"
        );
        assert_eq!(
            payload.get("decayed").and_then(|v| v.as_u64()),
            Some(1),
            "maintenance audit payload must record decayed count"
        );
    }

    // ── helper: extract text from ## heading to next ## or EOF ───────────────

    fn section_body<'a>(md: &'a str, heading: &str) -> &'a str {
        let start = match md.find(heading) {
            Some(pos) => pos + heading.len(),
            None => return "",
        };
        let rest = &md[start..];
        // Find the next ## section (but not the current heading's newline)
        let end = rest[1..]
            .find("\n## ")
            .map(|pos| pos + 1) // offset for the skipped char
            .unwrap_or(rest.len());
        &rest[..end]
    }
